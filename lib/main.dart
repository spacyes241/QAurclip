import 'package:flutter/material.dart';
import 'package:urclip_app/WelcomePage.dart';
import 'package:firebase_core/firebase_core.dart';
import 'firebase_options.dart';
import 'package:cloud_firestore/cloud_firestore.dart';

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );
  print('‚úÖ Firebase initialized');

  // üîç TEMP: test Firestore read
  try {
    final testSnapshot = await FirebaseFirestore.instance.collection('videos').limit(1).get();
    print('üî• Firestore test read success. Found ${testSnapshot.docs.length} docs.');
  } catch (e) {
    print('‚ùå Firestore test failed: $e');
  }

  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Flutter Demo',
      theme: ThemeData(
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.deepPurple),
        useMaterial3: true,
      ),
      home: const WelcomeScreen(),
    );
  }
}

